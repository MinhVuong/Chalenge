package me.service.model;

/**
 *
 * @author CPU01661-local
 */
public class News {
    int id;
    String content;
    int status;
    String time;

    public News() {
    }

    public News(int id, String content, int status, String time) {
        this.id = id;
        this.content = content;
        this.status = status;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
}
